export async function seedSampleProperties(accessToken: string, projectId: string) {
  const results = [];
  
  for (const property of sampleProperties) {
    try {
      const response = await fetch(
        `https://${projectId}.supabase.co/functions/v1/make-server-6a712830/properties`,
        {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${accessToken}`,
          },
          body: JSON.stringify(property),
        }
      );

      if (response.ok) {
        const data = await response.json();
        results.push({ success: true, property: data.property });
      } else {
        results.push({ success: false, error: await response.text() });
      }
    } catch (error) {
      results.push({ success: false, error: String(error) });
    }
  }

  return results;
}
